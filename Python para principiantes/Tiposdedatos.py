# TIPOS DE DATOS QUE SE MANEJAN EN PYTHON

# TIPO DE DATO ENTERO O LARGO
entero = 15
print(entero, type(entero) )

# TIPO DE DATO FLOTANTE O REAL 
real = 15.5
print(real, type(real))

# TIPO DE DATO COMPLEJO
complejo = 5 + 6j
print(complejo, type(complejo))

# TIPO DE DATO STRING O CARACTERES
nombre = 'Soy Batman'
print(nombre, type(nombre))

# TIPO DE DATO BOOLEANO 
trueorfalse = 3 == 3
print(trueorfalse, type(trueorfalse)) 