# La forma de realizar operaciones artimeticas desde python

#La Suma +
numero1 = 10
numero2 = 15
resultado = numero1 + numero2
resultado = str(resultado)
print("El resultado de la suma es: " + resultado)

#La Resta -
numero1 = 10
numero2 = 15
resultado = numero1 - numero2
resultado = str(resultado)
print("El resultado de la resta es: " + resultado)

#La Multiplicación *
numero1 = 9
numero2 = 2
resultado = numero1 * numero2
resultado = str(resultado)
print("El resultado de la multiplicación es: " + resultado)

#La Potenciación **
numero1 = 2
numero2 = 5
resultado = numero1 ** numero2
resultado = str(resultado)
print("El resultado de la potenciación es: " + resultado)

#La Division /
numero1 = 125
numero2 = 5
resultado = numero1 / numero2
resultado = str(resultado)
print("El resultado de la division es: " + resultado)

#El modulo o el resto % (Es el numero que le falta para llegar al numero que desea es decir:)
numero1 = 9
numero2 = 2
resultado = numero1 % numero2
resultado = str(resultado)
print("El resultado del modulo es: " + resultado)

#División entera //     -   Que nos muestre el numero entero sin decimales
numero1 = 9
numero2 = 2
resultado = numero1 // numero2
resultado = str(resultado)
print("El resultado de la division entera es: " + resultado)

